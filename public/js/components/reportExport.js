/**
 * Report Export Component — download reports in various formats
 */
const ReportExport = {
    report: null,

    setReport(report) {
        this.report = report;
    },

    init() {
        document.getElementById('export-json')?.addEventListener('click', () => this.exportJSON());
        document.getElementById('export-csv')?.addEventListener('click', () => this.exportCSV());
        document.getElementById('export-print')?.addEventListener('click', () => this.exportPrint());
    },

    exportJSON() {
        if (!this.report || !App.currentScanId) return;
        const url = api.getExportUrl(App.currentScanId, 'json');
        window.open(url, '_blank');
    },

    exportCSV() {
        if (!this.report || !App.currentScanId) return;
        const url = api.getExportUrl(App.currentScanId, 'csv');
        window.open(url, '_blank');
    },

    exportPrint() {
        if (!this.report) return;

        const printWin = window.open('', '_blank');
        const profile = this.report.profile || {};
        const summary = profile.summary || {};

        printWin.document.write(`
      <!DOCTYPE html>
      <html>
      <head>
        <title>OSINT Report — ${this.report.target}</title>
        <style>
          body { font-family: 'Inter', Arial, sans-serif; color: #1a1a1a; padding: 40px; max-width: 900px; margin: 0 auto; }
          h1 { font-size: 24px; border-bottom: 2px solid #7C4DFF; padding-bottom: 8px; }
          h2 { font-size: 18px; color: #333; margin-top: 24px; }
          table { width: 100%; border-collapse: collapse; margin: 12px 0; font-size: 13px; }
          th { background: #f5f5f5; text-align: left; padding: 8px; border: 1px solid #ddd; font-weight: 600; }
          td { padding: 8px; border: 1px solid #ddd; }
          .meta { color: #666; font-size: 13px; }
          .risk-badge { display: inline-block; padding: 4px 12px; border-radius: 20px; font-weight: 600; font-size: 13px; }
          .risk-critical { background: #FEE; color: #D32; }
          .risk-high { background: #FFF3E0; color: #E65; }
          .risk-medium { background: #FFFDE7; color: #F9A; }
          .risk-low { background: #E8F5E9; color: #2E7; }
          @media print { body { padding: 20px; } }
        </style>
      </head>
      <body>
        <h1>🔍 OSINT Reconnaissance Report</h1>
        <p class="meta">Target: <strong>${this.report.target}</strong> (${this.report.targetType})</p>
        <p class="meta">Generated: ${new Date().toLocaleString()}</p>
        <p class="meta">Risk Score: <span class="risk-badge risk-${(summary.riskLevel || 'low').toLowerCase()}">${summary.riskScore || 0} — ${summary.riskLevel || 'Low'}</span></p>

        <h2>Summary</h2>
        <table>
          <tr><td>Total Entities</td><td>${summary.totalEntities || 0}</td></tr>
          <tr><td>Total Relationships</td><td>${summary.totalRelationships || 0}</td></tr>
          <tr><td>Timeline Events</td><td>${summary.totalTimelineEvents || 0}</td></tr>
          <tr><td>Risk Factors</td><td>${summary.totalRiskFactors || 0}</td></tr>
        </table>

        ${(profile.riskFactors || []).length > 0 ? `
          <h2>Risk Factors</h2>
          <table>
            <thead><tr><th>Factor</th><th>Severity</th><th>Description</th></tr></thead>
            <tbody>
              ${profile.riskFactors.map(rf => `<tr><td>${rf.factor}</td><td>${rf.severity}</td><td>${rf.description}</td></tr>`).join('')}
            </tbody>
          </table>
        ` : ''}

        ${(profile.entities || []).length > 0 ? `
          <h2>Discovered Entities</h2>
          <table>
            <thead><tr><th>Type</th><th>Value</th><th>Source</th></tr></thead>
            <tbody>
              ${profile.entities.map(e => `<tr><td>${e.type}</td><td>${e.value || e.name || ''}</td><td>${e.source || ''}</td></tr>`).join('')}
            </tbody>
          </table>
        ` : ''}

        ${(profile.relationships || []).length > 0 ? `
          <h2>Relationships</h2>
          <table>
            <thead><tr><th>From</th><th>Type</th><th>To</th><th>Source</th></tr></thead>
            <tbody>
              ${profile.relationships.map(r => `<tr><td>${r.from}</td><td>${r.type}</td><td>${r.to}</td><td>${r.source}</td></tr>`).join('')}
            </tbody>
          </table>
        ` : ''}

        ${(profile.timeline || []).length > 0 ? `
          <h2>Timeline</h2>
          <table>
            <thead><tr><th>Date</th><th>Event</th><th>Source</th></tr></thead>
            <tbody>
              ${profile.timeline.map(t => `<tr><td>${t.date}</td><td>${t.event}</td><td>${t.source}</td></tr>`).join('')}
            </tbody>
          </table>
        ` : ''}

        <hr style="margin-top:32px">
        <p class="meta">Report generated by OSINT Recon Framework</p>
      </body>
      </html>
    `);

        printWin.document.close();
        setTimeout(() => printWin.print(), 500);
    },
};
